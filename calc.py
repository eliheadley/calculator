# create functionality for calculator
import math
# add two number
def add(x,y):
    return x + y

# substract two numbers
def subt(x,y):
    return x - y

# multiply two numbers
def mult(x,y):
    return x * y

# floating point division
def div(x,y):
    return x / y

# raise one number to power of another
def exponentiate(x,y):
    return x**y

# take square root of a number
def sqrt(x):
    return math.sqrt(x)